# Type integer atau bilangan bulat
data = 8
print(data)
print(type(data))

# Type float atau bilangan desimal
data = 8.0
print(data)
print(type(data))

# Type boolean atau biner
data1 = True
data2 = False
print(data1)
print(type(data1))

# Type string / kumpulan karakter
data1 = "Spiderman"
data2 = "8.0"
print(data1)
print(type(data1))
print(data2)
print(type(data2))

# Type data list / kumuplan elemen
# []
data = ["spiderman", "ironman"]
print(data)
print(type(data))

# Tuple 
# ()
data = ("spiderman", "ironman")
print(data)
print(type(data))

# Type data set
# {}
data = {"Spiderman", "Batman", "Batman"}
print(data)
print(type(data))

# Type data dictionary
data = {"spiderman": "Marvel", "Batman": "Dc"}
print(data)
print(type(data))